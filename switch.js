//-------------Javascript Switch--------------\\

var color = "yellow";

switch (color) {
  case "red":
    console.log("Your color is Red");
    break;
  case "blue":
    console.log("Your color is Blue");
    break;
  case "green":
    console.log("Your color is Green");
    break;
  case "yellow":
    console.log("Your color is Yellow");
    break;
  case "purple":
    console.log("Your color is Purple");
    break;
  case "magenta":
    console.log("Your color is Magenta");
    break;
}
