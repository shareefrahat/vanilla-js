//Sum of array using loop

const numbers = [37, 57, 29, 56, 20, 34, 23, 90];

let sum = 0;
for (let i = 0; i < numbers.length; i++) {
  const element = numbers[i];
  sum = sum + element;
}

console.log("The summation of numbers is =", sum);

